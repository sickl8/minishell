#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;
	size_t			i;

	i = 0;
	b = (unsigned char*)s2;
	a = (unsigned char*)s1;
	if (n == 0)
		return (0);
	while (a[i] == b[i] && i + 1 < n)
		i++;
	return (a[i] - b[i]);
}
