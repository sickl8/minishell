char	*ft_strrchr(const char *s, int c)
{
	char a;
	char *z;
	char *tmp;

	tmp = 0;
	z = (char*)s;
	a = (char)c;
	while (*z)
	{
		if (*z == a)
			tmp = z;
		z++;
	}
	if (a == '\0')
		return (z);
	return (tmp);
}
