#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long nb;
	long p;
	char c;

	nb = (long)n;
	p = 1;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb *= -1;
	}
	while (p * 10 <= nb)
		p *= 10;
	while (p > 0)
	{
		c = (nb / p) % 10 + 48;
		write(fd, &c, 1);
		p /= 10;
	}
}
