#include "libft.h"

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (!(*alst))
		*alst = new;
	else if (new)
	{
		new->next = *alst;
		*alst = new;
	}
	else if (*alst && !new)
		*alst = new;
}
