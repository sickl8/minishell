char	*ft_strchr(const char *s, int c)
{
	char a;
	char *z;

	z = (char*)s;
	a = (char)c;
	while (*z)
	{
		if (*z == a)
			return (z);
		z++;
	}
	if (a == '\0')
		return (z);
	return (0);
}
